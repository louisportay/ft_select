#include "libft.h"

int		ft_strmatch(const char *s1, const char *s2)
{
	int i;

	i = 0;
	while (*s1 == *s2)
	{
		if (*s1++ == '\0')
			return (i);
		s2++;
		i++;
	}
	return (i);
}
